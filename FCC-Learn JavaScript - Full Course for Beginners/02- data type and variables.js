/*
        Declaring variables
    Theres 3 way you can define variable in JS
    1. var
        It can be used throughout the program
    
    2. let
        It can be used within the scope where it is declared

    3. const
        It cannot be changed

    Example:
    var myName ;
    let myName ;
    const myName ;
*/